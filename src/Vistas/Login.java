    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   
        String pass=Password.getText();
        String usu=Usuario.getText();
        if(pass.isEmpty()|| usu.isEmpty()){
            JOptionPane.showMessageDialog(null, "rellena los campos");
            
        }else{
            List<modelo.Login>listaUsuarios=controller.findLoginEntities();
            boolean entro=false;
            for(modelo.Login l: listaUsuarios){
                if(l.getPassword().equals(pass)&& l.getUsuario().equals(usu)){
                    System.out.println("entro");
                    entro=true;
                    
                    
                    this.dispose();
                    new Principal().setVisible(true);
                }
            }
            if(entro==false){
                JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrecta");
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

